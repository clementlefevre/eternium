package com.model;


public class Imported_Product  {
 
	
	private String product_number ="";
	private String product_serial="";
	private String product_delivery_notes="";
	private String product_manufacturing_date="";
	private String product_qty_delivered="";
	private String product_revision_code="";
	private String importable="";
	
	
 
	public Imported_Product() {
	}



	public String getProduct_number() {
		return product_number;
	}



	public void setProduct_number(String product_number) {
		this.product_number = product_number;
	}



	public String getProduct_serial() {
		return product_serial;
	}



	public void setProduct_serial(String product_serial) {
		this.product_serial = product_serial;
	}



	public String getProduct_delivery_notes() {
		return product_delivery_notes;
	}



	public void setProduct_delivery_notes(String product_delivery_notes) {
		this.product_delivery_notes = product_delivery_notes;
	}



	public String getProduct_manufacturing_date() {
		return product_manufacturing_date;
	}



	public void setProduct_manufacturing_date(String product_manufacturing_date) {
		this.product_manufacturing_date = product_manufacturing_date;
	}



	public String getProduct_qty_delivered() {
		return product_qty_delivered;
	}



	public void setProduct_qty_delivered(String product_qty_delivered) {
		this.product_qty_delivered = product_qty_delivered;
	}



	public String getProduct_revision_code() {
		return product_revision_code;
	}



	public void setProduct_revision_code(String product_revision_code) {
		this.product_revision_code = product_revision_code;
	}



	public String getImportable() {
		return importable;
	}



	public void setImportable(String importable) {
		this.importable = importable;
	}
 
	
 
	
 
}